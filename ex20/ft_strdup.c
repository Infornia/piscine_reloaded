#include <stdlib.h>

char	*ft_strdup(char *s)
{
	char	*dup;
	int		i;

	i = 0;
	while (s[i])
		i++;
	dup = (char*)malloc(sizeof(*dup) * (i + 1));
	i = -1;
	while (s[++i])
		dup[i] = s[i];
	dup[i] = '\0';
	return (dup);
}
